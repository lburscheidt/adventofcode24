function getDiagonal(m) {
	let s,
		x,
		y,
		d,
		o = [],
		arr = [];
	for (s = 0; s < m.length; s++) {
		d = [];
		for (y = s, x = 0; y >= 0; y--, x++) d.push(m[y][x]);
		o.push(d);
	}
	for (s = 1; s < m[0].length; s++) {
		d = [];
		for (y = m.length - 1, x = s; x < m[0].length; y--, x++) d.push(m[y][x]);
		o.push(d);
	}
	for (el of o) {
		newEl = el.join("");
		arr.push(newEl);
	}
	return arr;
}

function reverseArr(arr) {
	const newArr = [];
	let newElement = [];

	for (element of arr) {
		for (let i = arr.length; i >= 0; i--) {
			newElement.push(element.charAt(i));
		}
		newElement = newElement.join("");
		newArr.push(newElement);
		newElement = [];
	}
	return newArr;
}

